double HarmonicOscillator_0::eval(double k, double k2, double *exp_factor, vec pos) {

    double H, x, y, r2;
    //1exp(-k**2*(x**2 + y**2)/2)
    x = pos(0); y = pos(1);
    H = 1;
    return H*(*exp_factor);
}


double dell_HarmonicOscillator_0_x::eval(double k, double k2, double *exp_factor, vec pos) {

    double H, x, y, r2;
    //-k2*xexp(-k**2*(x**2 + y**2)/2)
    x = pos(0); y = pos(1);
    H = -k2*x;
    return H*(*exp_factor);
}


double dell_HarmonicOscillator_0_y::eval(double k, double k2, double *exp_factor, vec pos) {

    double H, x, y, r2;
    //-k2*yexp(-k**2*(x**2 + y**2)/2)
    x = pos(0); y = pos(1);
    H = -k2*y;
    return H*(*exp_factor);
}


double lapl_HarmonicOscillator_0::eval(double k, double k2, double *exp_factor, vec pos) {

    double H, x, y, r2;
    //k2*(k2*r2 - 2)exp(-k**2*(x**2 + y**2)/2)
    x = pos(0); y = pos(1);
    r2 = x*x + y*y;
    H = k2*(k2*r2 - 2);
    return H*(*exp_factor);
}


